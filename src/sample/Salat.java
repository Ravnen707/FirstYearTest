package sample;

public class Salat {
    public int tal;
    public  String ord;

    public Salat(int tal, String ord) {
        this.tal = tal;
        this.ord = ord;
    }

    public int getTal() {
        return tal;
    }

    public void setTal(int tal) {
        this.tal = tal;
    }

    public String getOrd() {
        return ord;
    }

    public void setOrd(String ord) {
        this.ord = ord;
    }
}
