package sample;

/*
Obejct class lavet for ArrayList.
*/

public class Pasta {
    public int tal;
    public String ord;

    public Pasta(int tal, String ord) {
        this.tal = tal;
        this.ord = ord;
    }

    public int getTal() {
        return tal;
    }

    public void setTal(int tal) {
        this.tal = tal;
    }

    public String getOrd() {
        return ord;
    }

    public void setOrd(String ord) {
        this.ord = ord;
    }
}
/*
Brugt Getter and Setter for getTal til setOrd.
*/